    public void settable() {
        String[] columnNames = {"UserId", "SetId", "Attempted Questions", "Total Questions", "Correct Questions"};
        DefaultTableModel model = db.getAllResults();
        int data[][] = db.getResultdata();
        model.setColumnIdentifiers(columnNames);
        jTable_result.setModel(model);
        for (int i = 0; i < jTable_result.getRowCount(); i++) {
            for (int j = 0; j < 5; j++) {
                jTable_result.setValueAt(data[i][j], i, j);
            }
        }
    }

